package lab_4.Switch_Statements;

public class char1 {

    public static void main (String [] args) {
    char grade = 'A';

switch (grade) {
    case 'A': 
        System.out.println("Excellent!");
        break;
    case 'B':
        System.out.println("Good job!");
        break;
    default:
        System.out.println("Keep trying!");
}}
}
